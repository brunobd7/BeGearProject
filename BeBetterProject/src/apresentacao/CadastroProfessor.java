    private void bntConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntConsultarActionPerformed
        // TODO add your handling code here:
        
         try {
            String codigo = JOptionPane.showInputDialog("Digite o codigo do Professor para a consulta : ");
            
            if(codigo == null || codigo.isEmpty()){
                JOptionPane.showInputDialog("Digite o codigo do Professor para a consulta : "); 
            }
            
            int intCodigo = Integer.parseInt(codigo);
            EProfessores professor = new NProfessores().consultar(intCodigo);
            
            if(professor.getCodigo_professor()!=0){
                preencherTela(professor);
            }
            else{
                JOptionPane.showMessageDialog(null,"Professor não Cadastrado/Inexistente");
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"Informe um codigo válido!");
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_bntConsultarActionPerformed


