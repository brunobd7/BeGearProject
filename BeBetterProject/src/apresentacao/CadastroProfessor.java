    public void validarTela()throws Exception{
        
         if(txtNome.getText().isEmpty()){
            throw new Exception("É necessário preencher o campo Nome!");
        }
        if(txtCpf.getText().isEmpty()){
             throw new Exception("É necessário preencher o campo CPF!");
        }
        if(txtCertificacao.getText().isEmpty()){
             throw new Exception("É necessário preencher o campo de Certificação!");
        }
    }

    private void limparTela() {
        
        txtNome.setText("");
        txtCpf.setText("");
        txtCertificacao.setText("");
    }

    private void preencherTela(EProfessores professor) {
        
        
        txtNome.setText(professor.getNome()+ "");
        txtCpf.setText(professor.getCpf()+ "");
        txtCertificacao.setText(professor.getCertificado_cpr_aed()+ "");
        bntExcluir.setEnabled(true);
    }
