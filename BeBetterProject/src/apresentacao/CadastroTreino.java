    private void bntConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntConsultarActionPerformed
        // TODO add your handling code here:
        try {
                        
            String codigo = JOptionPane.showInputDialog("Digite o codigo do treino para consulta");
            
            if(codigo == null || codigo.isEmpty()){
                
                JOptionPane.showInputDialog("Digite o codigo do treino para consulta");
            }
            
            int intCodigo = Integer.parseInt(codigo);
            ETipoTreino treino = new NTipoTreino().consultar(intCodigo);
            
            if(treino.getCodigo_treino()!=0){
                preencherTela(treino);
            }
            else {
                JOptionPane.showMessageDialog(null,"Treino não cadastrado ou inexistente");
            }
            
            
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null,"Informe um codigo válido!");
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_bntConsultarActionPerformed

