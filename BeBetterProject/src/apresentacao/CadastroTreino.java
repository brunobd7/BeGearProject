    private void bntExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntExcluirActionPerformed
        // TODO add your handling code here:
        try {
             
            int confirma = (JOptionPane.showConfirmDialog(null,"Confirma a exclusão desse Treino ? , SysTrabson Training")
                + JOptionPane.YES_NO_OPTION);
            
            if(confirma == JOptionPane.YES_OPTION){
                int codigoTreino = Integer.parseInt(txtCodigoTreino.getText());
                new NTipoTreino().excluir(codigoTreino);
                
                JOptionPane.showMessageDialog(null, "Exclusão efetuada com sucesso");
                
                limparTela();
            }
            
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }//GEN-LAST:event_bntExcluirActionPerformed

    private void bntSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntSalvarActionPerformed
        // TODO add your handling code here:
        try {
            validarTela();
            
            ETipoTreino treino = new ETipoTreino();
            
            if(!txtCodigoTreino.getText().isEmpty()){
                treino.setCodigo_treino(Integer.parseInt(txtCodigoTreino.getText()));
            }
            
            treino.setNome_treino(txtNomeclaturaTreino.getText());
            treino.setDescricao(txtADescricao.getText());
            
            NTipoTreino nTreino = new NTipoTreino();
            nTreino.salvar(treino);
            
            limparTela();
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_bntSalvarActionPerformed


