    public void validarTela()throws Exception{
        
         if(txtNomeclaturaTreino.getText().isEmpty()){
            throw new Exception("Insira o nome do Treino!");
        }
        if(txtADescricao.getText().isEmpty()){
             throw new Exception("Insira a descrição do Treino!");
        }
    }

    private void limparTela() {
        
        txtNomeclaturaTreino.setText("");
        txtADescricao.setText("");
    }

    private void preencherTela(ETipoTreino treino) {
        
        
        txtNomeclaturaTreino.setText(treino.getNome_treino()+ "");
        txtADescricao.setText(treino.getDescricao()+ "");
        bntExcluir.setEnabled(true);
    }


